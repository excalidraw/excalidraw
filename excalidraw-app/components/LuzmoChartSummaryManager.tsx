/**
 * Hook that returns an animated ellipsis string that cycles: "", ".", "..", "..."
 */
function useAnimatedEllipsis(isActive: boolean): string {
  const [dotCount, setDotCount] = useState(0);

  useEffect(() => {
    if (!isActive) {
      setDotCount(0);
      return;
    }

    const interval = setInterval(() => {
      setDotCount((prev) => (prev + 1) % 4);
    }, ELLIPSIS_ANIMATION_SPEED_MS);

    return () => clearInterval(interval);
  }, [isActive]);

  return ".".repeat(dotCount);
}

