/**
 * Get a customer-facing name for the frame that contains the chart + AI summary.
 * Clearly indicates it's auto-generated by the chart with an AI summary.
 * Kept short for readability.
 */
function getChartSummaryFrameName(chart: ExcalidrawLuzmoChartElement): string {
  const title = chart.options?.title?.en?.trim();
  const base =
    title && title.length > 0
      ? title
      : chart.chartType
          .replace(/-/g, " ")
          .replace(/\b\w/g, (c) => c.toUpperCase());
  const name = `${base} — Chart + AI Summary`;
  return name.length > MAX_FRAME_NAME_LENGTH
    ? `${name.slice(0, MAX_FRAME_NAME_LENGTH - 3)}…`
    : name;
}
